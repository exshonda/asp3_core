/*
 *  FSBL(BL2) は FIP の bm-fw として本イメージをロードアドレス(既定 0x88000000)
 *  に読み込み，EL3 で entry(=_start) へジャンプする．本プログラムは USART2
 *  （TF-A が 115200 8N1 で初期化済み）へ "Connect using OpenOCD" を出力し，
 *  WFE ループで停止する．MMU/キャッシュは一切操作しない（OFF のまま）ため，
 *  OpenOCD からのプログラムロードがコヒーレントに行える．
 */

	.equ	USART2_BASE, 0x400E0000		/* ST-LINK 仮想COM */
	.equ	USART_ISR,   0x1C		/* 割込み・ステータスレジスタ */
	.equ	USART_TDR,   0x28		/* 送信データレジスタ */
	.equ	USART_ISR_TXE_BIT, 7		/* TXE/TXFNF（送信可）ビット */

	.section .text.start, "ax"
	.global _start
_start:
	/* 例外をマスク */
	msr	DAIFSet, #0xf

	/* USART2 のベースアドレスをロード（0x400E0000 = 0x400E << 16） */
	movz	x1, #0x400E, lsl #16

	/* メッセージ文字列の先頭 */
	adr	x0, msg

putc_loop:
	ldrb	w2, [x0], #1			/* 1文字取り出し，ポインタ++ */
	cbz	w2, hang			/* NUL なら終了 */
wait_txe:
	ldr	w3, [x1, #USART_ISR]		/* ISR 読み出し */
	tbz	w3, #USART_ISR_TXE_BIT, wait_txe	/* TXE=0 の間待つ */
	str	w2, [x1, #USART_TDR]		/* 送信 */
	b	putc_loop

	/* 出力後は停止（OpenOCD でハルト→ロードする土台） */
hang:
	wfe
	b	hang

	.align	3
msg:
	.asciz	"Connect using OpenOCD\r\n"
